	public void open(){
		
		System.out.println("Opening TestDataAccess instance ");

		String fileName=c.getDbFilename();
		
		if (c.isDatabaseLocal()) {
			  emf = Persistence.createEntityManagerFactory("objectdb:"+fileName);
			  db = emf.createEntityManager();
		} else {
			Map<String, String> properties = new HashMap<String, String>();
			  properties.put("javax.persistence.jdbc.user", c.getUser());
			  properties.put("javax.persistence.jdbc.password", c.getPassword());

			  emf = Persistence.createEntityManagerFactory("objectdb://"+c.getDatabaseNode()+":"+c.getDatabasePort()+"/"+fileName, properties);

			  db = emf.createEntityManager();
    	   }
		
	}
	public void close(){
		db.close();
		System.out.println("DataBase closed");
	}

	public boolean removeEvent(String description) {
		System.out.println(">> DataAccessTest: removeEvent");
		TypedQuery<Event> Equery = db.createQuery("SELECT e FROM Event e WHERE e.getDescription() =?1 ",Event.class);
		Equery.setParameter(1, description);
		List<Event> list= Equery.getResultList();
		Event e = list.get(0);
		if (e!=null) {
			db.getTransaction().begin();
			db.remove(e);
			db.getTransaction().commit();
			System.out.println(">> DataAccessTest: Event removed: " + description);
			return true;
		} else {
			System.out.println(">> DataAccessTest: Event removed: " + description);
			return false;
		}
    }
		
